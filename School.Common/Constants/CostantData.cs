        public static string getFieldId(Dictionary<string, string> dictValues, string selectedValue)
        {
            string _key = "";
            foreach (KeyValuePair<string, string> entry in dictValues)
            {
                if (entry.Value == selectedValue)
                {
                    _key = entry.Key;
                    return _key;
                }
            }
            return _key;
        }
        public static string getDictionaryValueByIndex(Dictionary<string, string> dictValues, int selectedValue)
        {
            string _value = "";
            int _index = 0;
            int dataSize = dictValues.Count;
            foreach (KeyValuePair<string, string> entry in dictValues)
            {
                if (_index == selectedValue)
                {
                    _value = entry.Value;
                    return _value;
                }
                _index++;
            }
            return _value;
        }


